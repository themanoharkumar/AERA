"""Standardized AI detection result model for AERA.

This module defines the DetectionResult class representing raw outputs
produced by the AI detector plugins.
"""

from typing import Any, Dict, List, Optional, Tuple


class DetectionResult:
    """Represents a standardized raw AI inference result from a detector plugin.

    A DetectionResult object encapsulates the raw output generated by AI models
    and is passed to the Event Manager for conversion to standardized Events. It
    does not run model inference or create business events directly.
    """

    def __init__(
        self,
        detection_id: str,
        detector_name: str,
        label: str,
        confidence: float,
        timestamp: float,
        inference_time: float,
        bounding_boxes: Optional[List[Tuple[int, int, int, int]]] = None,
        metadata: Optional[Dict[str, Any]] = None,
    ) -> None:
        """Initialize a new DetectionResult instance.

        Args:
            detection_id: Unique identifier for this detection (typically UUID).
            detector_name: The name of the detector plugin that produced this result.
            label: Detected object class/label (e.g., 'fire', 'smoke').
            confidence: Confidence score of the detection (float between 0.0 and 1.0).
            timestamp: Epoch timestamp when the frame was processed.
            inference_time: Inference execution duration in seconds.
            bounding_boxes: Optional list of bounding boxes as (xmin, ymin, xmax, ymax) tuples.
            metadata: Optional dictionary for dynamic/static metadata.
        """
        self.detection_id = detection_id
        self.detector_name = detector_name
        self.label = label
        self.confidence = confidence
        self.timestamp = timestamp
        self.inference_time = inference_time
        self.bounding_boxes = bounding_boxes if bounding_boxes is not None else []
        self.metadata = metadata if metadata is not None else {}

    def __repr__(self) -> str:
        """Return a string representation of the DetectionResult instance.

        Returns:
            A string format showing critical attributes of the detection result.
        """
        return (
            f"DetectionResult(id={self.detection_id!r}, detector={self.detector_name!r}, "
            f"label={self.label!r}, confidence={self.confidence:.2f}, boxes={len(self.bounding_boxes)})"
        )
